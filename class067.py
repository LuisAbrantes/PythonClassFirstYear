#criar um programa para somar o segundo com o terceiro número
#somar o quadrado do quinto número com o quadrado do terceiro número

lista = [1, 2, 3, 4, 5]

soma = lista[1]+lista[2]
print(f"A soma do segundo com o terceiro número é: {soma}.")

quadradoTres = lista[2]**2
quadradoCinco = lista[4]**2
somaQuadrado = quadradoCinco+quadradoTres
print(f"A soma dos quadrados do terceiro e do quinto número é: {somaQuadrado}.")