contador2 = 1
while(contador2<=10):
    print(f"Tabuada de soma do {contador2}")
    contador = 1
    while (contador <= 10):
        var = contador2 + contador 
        print(f"{contador2} + {contador} = {var}")
        contador += 1
    contador2 += 1

